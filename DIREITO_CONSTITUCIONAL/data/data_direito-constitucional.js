  <div class="callout callout-purple"><span class="callout-icon">📖</span> <strong>Para Leigos:</strong> Imagine que você é uma pessoa comum: pode ir ao shopping, comer pizza, assistir filme — ninguém proíbe! Mas o governo é diferente: só pode construir um hospital se a lei autorizar, só pode cobrar imposto se a lei permitir. Você tem liberdade; o governo tem limites. É como se você pudesse andar por toda a cidade, mas o governo só pode andar pelas ruas que o mapa (lei) permite.</div>
